game = scene:extend()

local p1, c
local loaders = {
  planet = planet,
  mine = mine,
}

function game:init()
  for _ = 1, 50 do
    star({ x = rnd(256) - 128, y = rnd(256) - 128 })
  end
  local data = LEVELS[LEVEL]
  local home = nil
  for _, tbl in ipairs(data) do
    local o = loaders[tbl.type](tbl)
    if tbl.text == "⌂" then home = o end
  end
  p1 = satellite({ x = 64, y = 64, target = home })
  task:every(30, function()
    local did_win = true
    tag.planet:walk(function(p)
      if p.text == "" then did_win = false end
    end)
    if did_win then scene:load(win) end
  end)
end

function game:did_init()
  c = { x = 0, y = 0 }
end

function game:update()
  tag.all:each("update")
  tag.mine:each("collides", p1, kill_player)

  local o = p1.target or p1
  c.x = lerp(c.x, o.x - 64, 0.1)
  c.y = lerp(c.y, o.y - 64, 0.1)
  camera(c.x, c.y)

  if btnp(5) then
    kill_player()
  end
end

function game:draw()
  cls()
  tag.bg:each("draw")
  tag.planet:each("draw")
  tag.mine:each("draw")
  tag.satellite:each("draw")
  tag.fg:each("draw")
  tag.ui:each("draw")
end

function kill_player()
  if p1.dead then return end
  p1.dead = true
  for _ = 1, 4 do
    fire({ x = p1.x + rnd(8) - 4, y = p1.y + rnd(8) - 4 })
  end
  p1:destroy()
  task:after(30, function() scene:load(title) end)
end
